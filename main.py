class BasicInterpreter:
    def __init__(self):
        self.variables = {}

    def eval_expr(self, expr):
        try:
            return eval(expr, {}, self.variables)
        except Exception as e:
            return f"Error: {e}"

    def assign_var(self, var, value):
        try:
            self.variables[var] = eval(value, {}, self.variables)
            return f"{var} = {self.variables[var]}"
        except Exception as e:
            return f"Error: {e}"

    def execute(self, command):
        if '=' in command:
            var, expr = command.split('=')
            var, expr = var.strip(), expr.strip()
            return self.assign_var(var, expr)
        else:
            return self.eval_expr(command)

def repl():
    interpreter = BasicInterpreter()
    while True:
        try:
            command = input(">>> ")
            if command.lower() in ['exit', 'quit']:
                break
            print(interpreter.execute(command))
        except KeyboardInterrupt:
            break

if __name__ == "__main__":
    repl()

